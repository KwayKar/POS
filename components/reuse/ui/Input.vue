<template>
  <input
    :type="type"
    :placeholder="placeholder"
    v-model="value"
    :class="'w-full px-4 py-2'"
    :style="inputStyle"
    :min="type === 'number' ? min : null"
    :max="type === 'number' ? max : null"
    :inputmode="type === 'number' ? 'numeric' : null"
    :readonly="readonly"
    @input="$emit('input', $event.target.value)"
  />
</template>

<script>
export default {
  name: "Input",
  props: {
    type: {
      type: String,
      default: "text",
    },
    placeholder: {
      type: String,
      default: "",
    },
    modelValue: {
      type: [String, Number],
    },
    error: {
      type: Boolean,
      default: false,
    },
    min: {
      type: [Number, String],
      default: null,
    },
    max: {
      type: [Number, String],
      default: null,
    },
    readonly: {
      type: Boolean,
      default: false,
    },
  },
  computed: {
    inputStyle() {
      return this.error ? { borderColor: "var(--red-1)" } : {};
    },
    value: {
      get() {
        return this.modelValue;
      },
      set(newValue) {
        this.$emit("update:modelValue", newValue);
      },
    },
  },
};
</script>

